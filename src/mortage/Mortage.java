package mortage;

public class Mortage {
    private int pricipal;
    private float rate;
    private int period;

    public int getPricipal() {
        return pricipal;
    }

    public void setPricipal(int pricipal) {
        this.pricipal = pricipal;
    }

    public float getRate() {
        return rate;
    }

    public void setRate(float rate) {
        this.rate = rate;
    }

    public int getPeriod() {
        return period;
    }

    public void setPeriod(int period) {
        this.period = period;
    }
}




